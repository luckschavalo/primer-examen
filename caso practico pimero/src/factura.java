public class factura {
    public String nombre;
    public String id;
    public int codigo;
    public Double salario; 

    public factura(String nombre, String id, int codigo, Double salario) {
        this.nombre = nombre;
        this.id = id;
        this.codigo = codigo;
        this.salario = salario;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public Double getSalario() {
        return salario;
    }

    public void setSalario(Double salario) {
        this.salario = salario;
    }
    
//se crea una clase para mas orden, ademas del uso de un constructor y de los setters & getters
    
}
